@certificates_bp.route("/api/cert/domains", methods=["GET"])
def cert_list_domains():
    domains = []
    for domain, config in CERT_DOMAINS.items():
        cert_info = {
            "domain": domain, "has_cert": False,
            "expires": None, "status": "none", "cert_count": 0,
        }
        cert_path = config["cert_path"]
        live_dir = os.path.join(cert_path, "live")
        if os.path.isdir(live_dir):
            cert_dirs = [d for d in os.listdir(live_dir)
                         if os.path.isdir(os.path.join(live_dir, d)) and d != "README"]
            cert_info["cert_count"] = len(cert_dirs)

        fullchain_path = find_cert_fullchain(cert_path)
        if fullchain_path:
            cert_info["has_cert"] = True
            cert_info["status"] = "valid"
            if cert_info["cert_count"] == 0:
                cert_info["cert_count"] = 1
            try:
                result = subprocess.run(
                    ["openssl", "x509", "-enddate", "-noout", "-in", fullchain_path],
                    capture_output=True, text=True, timeout=5,
                )
                if result.returncode == 0 and "notAfter=" in result.stdout:
                    cert_info["expires"] = result.stdout.strip().split("notAfter=")[1]
            except Exception:
                pass
        domains.append(cert_info)
    return jsonify({"domains": domains})


@certificates_bp.route("/api/cert/domains/detail", methods=["GET"])
def cert_list_domains_detail():
    result = []
    for domain, config in CERT_DOMAINS.items():
        cert_path = config["cert_path"]
        certs = []
        live_dir = os.path.join(cert_path, "live")
        if os.path.isdir(live_dir):
            for entry in sorted(os.listdir(live_dir)):
                sub_path = os.path.join(live_dir, entry)
                if os.path.isdir(sub_path) and entry != "README":
                    fc = os.path.join(sub_path, "fullchain.pem")
                    if os.path.isfile(fc):
                        certs.append((entry, fc))
        else:
            for fc in sorted(glob.glob(os.path.join(cert_path, "fullchain*.pem"))):
                if os.path.isfile(fc):
                    certs.append((domain, fc))
                    break

        cert_details = []
        for name, fc_path in certs:
            info = {"name": name, "expires": None, "issued": None,
                    "days_remaining": None, "status": "unknown",
                    "serial": None, "issuer": None}
            try:
                out = subprocess.run(
                    ["openssl", "x509", "-noout",
                     "-enddate", "-startdate", "-serial", "-issuer",
                     "-in", fc_path],
                    capture_output=True, text=True, timeout=5,
                )
                if out.returncode == 0:
                    for line in out.stdout.strip().splitlines():
                        if line.startswith("notAfter="):
                            info["expires"] = line.split("=", 1)[1]
                        elif line.startswith("notBefore="):
                            info["issued"] = line.split("=", 1)[1]
                        elif line.startswith("serial="):
                            info["serial"] = line.split("=", 1)[1]
                        elif line.startswith("issuer="):
                            info["issuer"] = line.split("=", 1)[1].strip()
                    if info["expires"]:
                        try:
                            exp_dt = datetime.datetime.strptime(
                                info["expires"], "%b %d %H:%M:%S %Y %Z"
                            ).replace(tzinfo=datetime.timezone.utc)
                            now = datetime.datetime.now(datetime.timezone.utc)
                            info["days_remaining"] = (exp_dt - now).days
                            if info["days_remaining"] < 0:
                                info["status"] = "expired"
                            elif info["days_remaining"] < 7:
                                info["status"] = "critical"
                            elif info["days_remaining"] < 30:
                                info["status"] = "expiring_soon"
                            else:
                                info["status"] = "valid"
                        except ValueError:
                            pass
            except Exception:
                pass
            cert_details.append(info)

        total = len(cert_details)
        valid = sum(1 for c in cert_details if c["status"] == "valid")
        expiring = sum(1 for c in cert_details if c["status"] in ("expiring_soon", "critical"))
        days_list = [c["days_remaining"] for c in cert_details if c["days_remaining"] is not None]
        earliest_days = min(days_list) if days_list else None
        earliest_expiry = None
        if earliest_days is not None:
            for c in cert_details:
                if c["days_remaining"] == earliest_days:
                    earliest_expiry = c["expires"]
                    break

        result.append({
            "domain": domain, "certs": cert_details,
            "summary": {
                "total": total, "valid": valid, "expiring_soon": expiring,
                "earliest_expiry": earliest_expiry, "earliest_days": earliest_days,
            },
        })
    return jsonify({"domains": result})


