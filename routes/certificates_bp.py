@certificates_bp.route("/api/cert/deploy-vthunder", methods=["POST"])
def api_cert_deploy_vthunder():
    """Deploy a certificate directly to a vThunder device."""
    data = request.json or {}
    base_domain = data.get("base_domain", "").strip()
    domain = data.get("domain", "").strip()
    cert_file = data.get("cert_file", "fullchain.pem").strip()
    vthunder_host = data.get("vthunder_host", "").strip()
    vthunder_group = data.get("vthunder_group", "").strip()
    partition = data.get("partition", "").strip()

    if not all([base_domain, domain, vthunder_host, vthunder_group, partition]):
        return api_error("base_domain, domain, vthunder_host, vthunder_group, and partition are required")

    # Validate cert file
    if cert_file not in ("fullchain.pem", "cert.pem"):
        return api_error("cert_file must be fullchain.pem or cert.pem")

    # Resolve cert path
    cert_path, err, status = _resolve_cert_path(base_domain, domain, cert_file)
    if err:
        return api_error(err, status)
    key_path, err, status = _resolve_cert_path(base_domain, domain, "privkey.pem")
    if err:
        return api_error(f"Private key not found: {err}", status)

    # Resolve vThunder credentials from inventory
    creds = get_vthunder_host_credentials("inventory.yml", vthunder_group, vthunder_host)
    if not creds:
        return api_error(f"Could not find credentials for {vthunder_host} in {vthunder_group}")

    # Derive cert name
    cert_name = domain.replace(".", "_").replace("*", "wildcard")

    # Check if cert already exists on device (renewal detection)
    is_renewal = False
    try:
        existing_certs = _get_vthunder_certs(
            host=vthunder_host, username=creds["username"],
            password=creds["password"], partition=partition, port=creds["port"],
        )
        existing_names = [c.get("file", "") for c in existing_certs]
        is_renewal = any(cert_name in name for name in existing_names)
    except Exception:
        pass  # Non-fatal — we'll still attempt the upload

    # Run upload helper
    helper = os.path.join(ROOT_DIR, "python", "helpers", "upload_cert_to_vthunder.py")
    cmd = [
        "python3", helper,
        "--host", vthunder_host,
        "--port", str(creds["port"]),
        "--username", creds["username"],
        "--password", creds["password"],
        "--partition", partition,
        "--cert-name", cert_name,
        "--cert-file", cert_path,
        "--key-file", key_path,
    ]

    try:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=60,
                                cwd=os.path.join(ROOT_DIR, "python"))
        output_lines = result.stdout.strip().split("\n") if result.stdout else []
        success = result.returncode == 0
        CERT_DEPLOYS.labels(status="success" if success else "failed").inc()

        action = "renewed" if is_renewal else "deployed"
        audit_log("cert_deploy_vthunder", f"{cert_name} → {vthunder_host}/{partition}",
                  f"domain={domain} action={action} success={success}")

        if success:
            return jsonify({
                "success": True,
                "cert_name": cert_name,
                "is_renewal": is_renewal,
                "message": f"Certificate '{cert_name}' {'renewed' if is_renewal else 'deployed'} on {vthunder_host}/{partition}",
                "output": output_lines,
            })
        else:
            err_detail = result.stderr.strip() or result.stdout.strip() or 'Unknown error'
            return api_error(
                f"Upload failed: {err_detail}",
                500,
            )
    except subprocess.TimeoutExpired:
        return api_error("Upload timed out after 60 seconds", 504)
    except Exception as e:
        return api_error(str(e), 500)
