def find_cert_fullchain(cert_path):
    """Find fullchain.pem in various possible locations.

    Checks live/ first (certbot's canonical location for current certs),
    then falls back to root-level files (legacy/non-certbot layouts).
    """
    live_pattern = os.path.join(cert_path, "live", "*", "fullchain.pem")
    live_certs = glob.glob(live_pattern)
    if live_certs:
        return max(live_certs, key=os.path.getmtime)
    direct_pattern = os.path.join(cert_path, "fullchain*.pem")
    direct_certs = [f for f in glob.glob(direct_pattern) if os.path.isfile(f)]
    if direct_certs:
        return max(direct_certs, key=os.path.getmtime)
    return None


def _resolve_cert_path(base_domain, name, filename):
    """Resolve cert file path with security validation."""
    if base_domain not in CERT_DOMAINS:
        return None, "Invalid base domain", 400
    if filename not in ALLOWED_CERT_FILES:
        return None, "File not allowed", 400
    if ".." in name or "/" in name:
        return None, "Invalid cert name", 400
    cert_dir = CERT_DOMAINS[base_domain]["cert_path"]
    file_path = os.path.join(cert_dir, "live", name, filename)
    real = os.path.realpath(file_path)
    if not real.startswith(os.path.realpath(cert_dir)):
        return None, "Path traversal blocked", 400
    if not os.path.isfile(real):
        return None, "File not found", 404
    return real, None, None


def generate_cert_playbook(base_domain, domains, cert_path):
    """Generate Ansible playbook YAML for certificate deployment."""
    playbook = f"""---
# Ansible Playbook: Deploy SSL Certificate for {base_domain}
# Generated by Q Branch Automation Demo
# Domains: {', '.join(domains)}

- name: Deploy SSL Certificate - {base_domain}
  hosts: all
  become: yes
  vars:
    cert_source_path: "{cert_path}"
    cert_dest_path: "/etc/ssl/certs"
    key_dest_path: "/etc/ssl/private"

  tasks:
    - name: Ensure SSL directories exist
      file:
        path: "{{{{ item }}}}"
        state: directory
        mode: '0755'
      loop:
        - "{{{{ cert_dest_path }}}}"
        - "{{{{ key_dest_path }}}}"

    - name: Copy fullchain certificate
      copy:
        src: "{{{{ cert_source_path }}}}/fullchain.pem"
        dest: "{{{{ cert_dest_path }}}}/{base_domain}-fullchain.pem"
        mode: '0644'
        owner: root
        group: root
      notify: Reload web service

    - name: Copy private key
      copy:
        src: "{{{{ cert_source_path }}}}/privkey.pem"
        dest: "{{{{ key_dest_path }}}}/{base_domain}-privkey.pem"
        mode: '0600'
        owner: root
        group: root
      notify: Reload web service

  handlers:
    - name: Reload web service
      service:
        name: nginx  # Change to your web server (apache2, httpd, etc.)
        state: reloaded
"""
    return playbook


